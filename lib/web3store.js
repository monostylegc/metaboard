const contractaddress = '0xFC22B149c5b0722B239662168d3e142DC010dAeE'

const gasLimit = 285000

export const useWeb3 = create((set,get)=>({
    web3: null,
    contract: null,
    provider : null,
	clamming : false,
    async loadProvider(){
        const provider = await detectEthereumProvider()

        if(provider){
            const web3 = new Web3(provider)
            const contract = new web3.eth.Contract(abi, contractaddress)
			
            provider.on("chainChanged",()=>window.location.reload())

            set({web3})
            set({contract})
            set({provider})
        }
        else {
          console.log("plz install metamask")
        }
    },
    connect: async function () {
      const provider = await detectEthereumProvider()
    
      if(provider){
        try {
          await provider.request({method : "eth_requestAccounts"})
        } catch {
          location.reload()
        }
      } else {
        console.log("plz install metamask")
      }
    },
	mint : async function (id) {
		const contract = get().contract
		const web3 = get().web3
		const account = await web3.eth.getAccounts()

		set({clamming:true})

		contract.methods.mint(id)
		.send({
			gasLimit:gasLimit,
			to:contractaddress,
			from: `${account}`,
			value: web3.utils.toWei('1','ether')
		})
		.once("error", (err)=>{
			console.log(err)
			set({clamming : false})
			//notify해주자
		})
		.then((res)=>{
			console.log(res)
			set({clamming:false})
			//성공 알림
		})
	},
	changeImgUrl : async function(id, url, account) {
		const contract = get().contract

		contract.methods.setTileImgUrl(id, url).send({
            from:`${account}`,
            gasLimit: gasLimit
        }).once("error", (err)=>{
			console.log(err)
			set({clamming : false})
			//notify해주자
		})
		.then((res)=>{
			console.log(res)
			set({clamming : false})
			//성공시 알림
		})
	}
}))